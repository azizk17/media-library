import React from "react";
import {
  FacebookIcon,
  InstagramIcon,
  LinkedinIcon,
  TwitchIcon,
  TwitterIcon,
  YoutubeIcon,
} from "lucide-react";

interface PlatformIconProps {
  name: string;
  className?: string;
}

export const PlatformIcon = ({ name, className }: PlatformIconProps) => {
  const getIcon = () => {
    switch (name?.toLowerCase()) {
      case "facebook":
        return <FacebookIcon className={className} />;
      case "twitter":
        return <TwitterIcon className={className} />;
      // Add cases for other social media platforms
      case "instagram":
        return <InstagramIcon className={className} />;
      case "youtube":
        return <YoutubeIcon className={className} />;
      case "tiktok":
        return <TiktokIcon className={className} />;
      case "snapchat":
        return <SnapchatIcon className={className} />;
      case "linkedin":
        return <LinkedinIcon className={className} />;
      case "reddit":
        return <RedditIcon className={className} />;
      case "telegram":
        return <TelegramIcon className={className} />;
      case "whatsapp":
        return <WhatsappIcon className={className} />;
      case "discord":
        return <DiscordIcon className={className} />;
      case "twitch":
        return <TwitchIcon className={className} />;
      default:
        return name;
    }
  };

  return <div>{getIcon()}</div>;
};

